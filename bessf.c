double chebev(double x, double *c, int r)
{
	double	d = 0.0, dd = 0.0, t2, sv;
	int		j;

	t2 = x + x;

	for(j = r - 1; j > 0; j--)
	{
		sv = d; d = t2 * d - dd + c[j]; dd = sv;
	}

	return x * d - dd + 0.5 * c[0];
};

void bess(double x, int nmax, double *bj, double *by)
{
	double	xn, tt, p, q, cx, sx, j0, y0, j1, y1;
	int		i, j;

	if(x == 0.0)
	{
		for(i = 0; i <= nmax; i++)
		{
			*(bj + i) = 0.0; *(by + i) = (x < 0.0 && (i & 1) ? db_max : -db_max);
		}

		*bj = 1.0; return;
	}

	if(x < 8.0)
	{
		xn = x / 8.0; tt = 2.0 * xn * xn - 1.0; 
		
		j0 = chebev(tt, j0lo, 18); j1 = xn * chebev(tt, j1lo, 18);

		y0 = chebev(tt, y0lo, 18); y1 = xn * chebev(tt, y1lo, 18);

		y0 += 2.0 * log(x) * j0 / pi; y1 += 2.0 * (log(x) * j1 - 1.0 / x) / pi;
	}
	else
	{
		xn = 8.0 / x; tt = 2.0 * xn * xn  - 1.0; cx = sqrt(2.0 / (pi * x));

		sx = cx * sin(x - 0.25 * pi); cx *= cos(x - 0.25 * pi);
	
		p = chebev(tt, jy0hip, 18); q = xn * chebev(tt, jy0hiq, 18);

		j0 = p * cx - q * sx; y0 = p * sx + q * cx;

		p = chebev(tt, jy1hip, 18); q = xn * chebev(tt, jy1hiq, 18);

		j1 = q * cx + p * sx;  y1 = q * sx - p * cx;

	}

	/* generate Y[n] using upward recurrence from Y[0] and Y[1] (always stable)  */

	for(i = 0; i <= nmax; i++)
	{
		*(by + i) = y0; tt = ((i + i + 2) / x) * y1 - y0; 
		
		if(!_finite(tt))

			break;

		y0 = y1; y1 = tt; 
	}

	for(j = i + 1; j <= nmax; j++)		/* if overflow fill rest of array with db_max */
	{	
		*(by + j) = y1; y1 = (x < 0.0 && !(j & 1) ? db_max : -db_max);
	}

	if(nmax < 0.8 * fabs(x))			/* generate J[n] using upward recurrence    */ 
	{									/* (stable while n < |x|)                   */
		for(i = 0; i <= nmax; i++)
		{
			*(bj + i) = j0; tt = ((i + i + 2) / x) * j1 - j0; j0 = j1; j1 = tt; 
		}
	}
	else			        /* generate using downward recurrence (always stable)   */
	{	
		/* first continue to generate y[n] upwards to find the  */
		/* point at which to start downward recurrence for J[n] */
		
		y1 /= y0; y0 = 1.0;

		for( ;fabs(y0) < 1.0e20 || (i & 1); i++)
		{
			tt = ((i + i + 2) / x) * y1 - y0; y0 = y1; y1 = tt; 
		}

		y0 = 1.0 / (i * y0); y1 = 1.0 / ((i + 1) * y1);

		for(j = nmax; j >= i; j--)	/* fill array with zero values if necessary */

			*(bj + j) = 0.0; 

		for(p = 0.0; i > 0; i--)	/* generate J[n] downwards from starting    */ 
		{							/* estimates based on Y[i] and Y[i + 1]     */
			if(i <= nmax)

				*(bj + i) = y0;		/* output answer if required    */
						
			if(!(i & 1))

				p += y0;			/* accumulate normalisation sum */

			tt = ((i + i) / x) * y0 - y1; y1 = y0; y0 = tt;

		}

		*bj = y0; p = 1.0 / (y0 + 2.0 * p);
			
		for(i = 0; i <= nmax; i++)
			
			*(bj + i) *= p;
	}
};
